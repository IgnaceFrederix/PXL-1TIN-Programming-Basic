package voorbeelden;


public class voorbeeld1 {
	    
	    public static void main(String[] args) {
	    	boolean bool = true;
	    	char achar = 'a';
	    	byte aByte = 20;
	    	short aShort = 256;
	    	int aInt = 369;
	    	double aDouble = 3.256;
	    	final double PI = 3.14;// een final kan je nooit meer aanpassen als er een waarde aan gegeven is
	    	float aFloat = 1.25451F;
	    	long aLong = 4541541651615L;
	    	System.out.println("een boolean " + bool);
	    	System.out.println("een karakter " + achar);
	    	System.out.println("een byte " + aByte);
	    	System.out.println("een short " + aShort);
	    	System.out.println("een int " + (aInt +1));
	    	System.out.println("een double " + aDouble);
	    	System.out.println("de waarde van PI " + PI);
	    	System.out.println("de waarde van een float" + aFloat);
	    	System.out.println("de waarde van een long " + aLong);
	    }

}
